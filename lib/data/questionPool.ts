import { Question } from './types';

// Deterministic PRNG based on seed string to keep questions perfectly static and identical across reloads
function getSeedValue(str: string): number {
  let hash = 0;
  for (let i = 0; i < str.length; i++) {
    hash = (hash << 5) - hash + str.charCodeAt(i);
    hash |= 0;
  }
  // LCG-like calculation to get a number between 0 and 1
  const x = Math.sin(hash++) * 10000;
  return x - Math.floor(x);
}

// Generates a deterministically random integer between min and max
function randomIntFromSeed(seed: string, min: number, max: number): number {
  const r = getSeedValue(seed);
  return Math.floor(r * (max - min + 1)) + min;
}

// Helper to shuffle or select from items deterministically
function selectFromSeed<T>(seed: string, arr: T[]): T {
  const idx = randomIntFromSeed(seed, 0, arr.length - 1);
  return arr[idx];
}

export function getQuestionsPoolForLesson(lessonId: string, initialQuestions: Question[] = []): Question[] {
  const pool: Question[] = [];

  // 1. Add any initial handcrafted questions first
  initialQuestions.forEach((q) => {
    // Standardize IDs
    pool.push({
      ...q,
      id: q.id
    });
  });

  // Target count of 100 questions per lesson
  const targetCount = 100;
  const remainingCount = targetCount - pool.length;

  if (remainingCount <= 0) {
    return pool;
  }

  // Detect subject discipline from the lesson ID prefix
  let subjectType: 'physics' | 'chemistry' | 'biology' | 'geology' | 'math' | 'history' | 'geography' = 'physics';
  if (lessonId.includes('_c_') || lessonId.includes('chemistry')) {
    subjectType = 'chemistry';
  } else if (lessonId.includes('_b_') || lessonId.includes('biology')) {
    subjectType = 'biology';
  } else if (lessonId.includes('_geo_') || lessonId.includes('geology')) {
    subjectType = 'geology';
  } else if (lessonId.includes('_calc_') || lessonId.includes('calculus')) {
    subjectType = 'math';
  } else if (lessonId.includes('_h_') || lessonId.includes('history')) {
    subjectType = 'history';
  } else if (lessonId.includes('_g_') || lessonId.includes('geography')) {
    subjectType = 'geography';
  }

  // Generate remaining questions deterministically
  for (let i = 0; i < remainingCount; i++) {
    const qIndex = pool.length + 1;
    const seed = `${lessonId}_q_${qIndex}`;
    
    // Categorize difficulty levels
    // 1 to 33: Easy, 34 to 70: Medium, 71 to 100: Hard
    let difficulty: 'easy' | 'medium' | 'hard' = 'easy';
    if (qIndex > 33 && qIndex <= 70) {
      difficulty = 'medium';
    } else if (qIndex > 70) {
      difficulty = 'hard';
    }

    let generatedQuestion: Question;

    // Dispatcher for deterministic question creation based on subject and difficulty
    switch (subjectType) {
      case 'physics':
        generatedQuestion = generatePhysicsQuestion(lessonId, qIndex, seed, difficulty);
        break;
      case 'chemistry':
        generatedQuestion = generateChemistryQuestion(lessonId, qIndex, seed, difficulty);
        break;
      case 'biology':
        generatedQuestion = generateBiologyQuestion(lessonId, qIndex, seed, difficulty);
        break;
      case 'geology':
        generatedQuestion = generateGeologyQuestion(lessonId, qIndex, seed, difficulty);
        break;
      case 'math':
        generatedQuestion = generateMathQuestion(lessonId, qIndex, seed, difficulty);
        break;
      case 'history':
        generatedQuestion = generateHistoryQuestion(lessonId, qIndex, seed, difficulty);
        break;
      case 'geography':
        generatedQuestion = generateGeographyQuestion(lessonId, qIndex, seed, difficulty);
        break;
      default:
        generatedQuestion = generatePhysicsQuestion(lessonId, qIndex, seed, difficulty);
    }

    pool.push(generatedQuestion);
  }

  return pool;
}

